def code_short_title(code: str) -> str | None:
    """
    Return a short human-readable title for an FDA Food Code section number.

    Strips subitems from right to left until a match is found, e.g.:
      '3-501.14(A)(1)' → '3-501.14(A)' → '3-501.14' → 'Cooling'
    Returns None if no match (caller should fall back to full description).
    """
    if not code:
        return None
    import re as _re
    current = code.strip()
    # Normalize case of the base code (e.g. '3-501.14(a)' → keep, just strip subitems)
    for _ in range(6):
        title = FDA_SHORT_TITLES.get(current)
        if title:
            return title
        stripped = _re.sub(r'\s*\([^)]*\)\s*$', '', current).strip()
        if stripped == current:
            break
        current = stripped
    return None
