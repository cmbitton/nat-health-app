def item_severity(item_number: int) -> str:
    """Return 'critical', 'major', or 'minor' for an FDA inspection item number."""
    if item_number in PRIORITY_ITEMS:
        return 'critical'
    if item_number in PRIORITY_FOUNDATION_ITEMS:
        return 'major'
    return 'minor'

